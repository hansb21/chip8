void init(Chip8* chip8) 
{

	chip8->pc = 0x200;
	chip8->opcode = 0;
	chip8->I = 0;
	chip8->sp = 0;
	#ifdef DEBUG
		printf("loading fontset\n"); 
	#endif
	for (int i = 0; i < 80; ++i)
		chip8->memory[i] = chip8_fontset[i];
}
